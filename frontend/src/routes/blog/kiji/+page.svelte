<h1 style="color: #8f2e14;">☕掲示板について<em><span style="color: #999999;">(編集中)</span></em></h1>
<p>エッヂコンピューティング化されたサーバーにCAPTCHA認証を取り付けた現代的な匿名掲示板です。海外オフショア鯖を利用しています。</p>
<h2>特色</h2>
<ul>
<li>板の作成機能</li>
<li>板のモデレーション機能(アカウント・IPアドレスに基づくBANができます)</li>
<li>デフォルトでのワッチョイ</li>
<li>レベル表示(現在はLv.10が上限)</li>
<li>必死チェッカー、過去ログ倉庫の標準搭載</li>
<li>&nbsp;</li>
</ul>
<p>ネームド荒らしや自演行為、スクリプト、DDoS攻撃等の匿名掲示板における慢性的な脆弱性の克服を目指しました。</p>
<p>現在はテスト版を公開しています。不具合があったらごめん。</p>
