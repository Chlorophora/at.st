<h2 style="color: #2e6c80;"><span style="color: #000000;">BAN機能</span></h2>
<p>板の作成者はモデレート権限を持ちます。あなたの作成した板をブラウザで開いて各種設定をしてください</p>
<p>　&alpha;仕様：板の作成者のみがBAN権限を持ちます</p>
<p>　&beta;仕様：板の作成者とスレッド作成者がBAN権限を持ちます</p>
<p>ブラウザでIDをクリックするとBANできます</p>
<p><a style="color: #0000ff;" href="https://afternoontea.st/my/bans">https://afternoontea.st/my/bans</a>からBAN履歴を閲覧できます</p>
